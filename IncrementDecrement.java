//this program shows increment decrements

public class IncrementDecrement
{
   public static void main (String[]args)
   {
      int num=1;      
      System.out.println(num);
      
      num++;
      System.out.println(num);
      
      num--;
      System.out.println(num);
      
      System.out.println(++num);
      System.out.println(num++);
      System.out.println(num);
   }
}